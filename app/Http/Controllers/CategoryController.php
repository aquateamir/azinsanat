    /**
     * Display the specified resource.
     *
     * @param  \App\Models\Category  $category
     * @return \Illuminate\Http\Response
     */
    public function show(Category $category)
    {
        $category = Category::find($category);
        foreach ($category as $result){
            SEOTools::setTitle(" دسته بندی ".$result->name);
            SEOTools::setDescription($result->name);
            SEOTools::opengraph()->setUrl('https://soorinaz.ir/category/'.$result->slug);
            SEOTools::setCanonical('https://soorinaz.ir/category/'.$result->slug);
            SEOTools::twitter()->setSite("@soorinaz");
            SEOTools::opengraph()->addProperty('type', 'website');
            SEOMeta::addKeyword([$result->name]);
        }

        return  view('category.all', compact('category'));
    }

